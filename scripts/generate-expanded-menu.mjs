const slug = (store, name) =>
  `${store}-${name}`
    .toLowerCase()
    .replace(/[^a-z0-9\u4e00-\u9fff]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 60)

/** @param {Partial<{role:string,portionable:boolean,tags:string[]}>} extra */
function mk(store, source, category, name, cal, pro, carb, fat, price, desc, extra = {}) {
  return {
    id: slug(store, name),
    name,
    store,
    source,
    category,
    role: extra.role ?? 'combo',
    portionable: extra.portionable ?? false,
    tags: extra.tags ?? [],
    calories: cal,
    protein_g: pro,
    carbs_g: carb,
    fat_g: fat,
    price,
    photo_url: '',
    description: desc,
  }
}

