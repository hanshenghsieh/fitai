const SOFT_SUFFIXES = [
  '',
  ' 我在。',
  ' 慢慢來。',
  ' 不用急。',
  ' 明天見。',
  ' 好。',
  ' 嗯。',
  ' 就這樣。',
  ' 可以了。',
  ' 夠了。',
]

function buildCompanionLines(): CompanionLine[] {
  const lines: CompanionLine[] = []
  const seen = new Set<string>()
  let id = 0

  const add = (text: string, category: CompanionCategory) => {
    const t = text.trim()
    if (!t || BANNED.test(t) || seen.has(t)) return
    seen.add(t)
    lines.push({ id: `companion-${id++}`, text: t, category })
  }

  for (const [category, stems] of Object.entries(CATEGORY_STEMS) as [CompanionCategory, string[]][]) {
    for (const stem of stems) {
      add(stem, category)
      for (const suffix of SOFT_SUFFIXES) {
        if (!suffix) continue
        add(`${stem.replace(/[。！？]$/, '')}${suffix}`, category)
      }
    }
  }

  // Pad to 500 with safe combinations
  const prefixes = ['今天', '今晚', '此刻', '這幾天', '最近', '明天', '身體', '日子', '節奏', '我']
  const middles = [
    '慢慢來',
    '不用急',
    '我在',
    '夠了',
    '可以休息',
    '照常就好',
    '交給我',
    '先喝水',
    '先睡吧',
    '還在',
  ]
  const ends = ['。', '，好。', '，嗯。', '就好。', '吧。']

  for (const p of prefixes) {
    for (const m of middles) {
      for (const e of ends) {
        if (lines.length >= 500) break
        add(`${p}${m}${e}`, 'gentle')
      }
    }
  }

  while (lines.length < 500) {
    const n = lines.length
    add(`今天第 ${n} 次想跟你說：慢慢來就好。`, 'gentle')
  }

  return lines.slice(0, 500)
}

export const COMPANION_LINES: CompanionLine[] = buildCompanionLines()
